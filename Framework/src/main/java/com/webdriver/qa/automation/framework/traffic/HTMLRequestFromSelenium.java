package com.webdriver.qa.automation.framework.traffic;

import java.util.List;

public class HTMLRequestFromSelenium {

	private int statusCode;
	private String method;
	private String url;
	private int bytes;
	private String start;
	private String end;
	private int timeInMillis;
	private List<ValuePair> requestHeaders;	

	public int getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(int statusCode) {
		this.statusCode = statusCode;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getBytes() {
		return bytes;
	}
	public void setBytes(int bytes) {
		this.bytes = bytes;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public int getTimeInMillis() {
		return timeInMillis;
	}
	public void setTimeInMillis(int timeInMillis) {
		this.timeInMillis = timeInMillis;
	}
	public List<ValuePair> getRequestHeaders() {
		return requestHeaders;
	}
	public void setRequestHeaders(List<ValuePair> requestHeaders) {
		this.requestHeaders = requestHeaders;
	}
}